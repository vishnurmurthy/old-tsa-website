<h3>TSA Events</h3>

<p>TSA offers a variety of competitive events in a wide range of technical fields.  The events are described below in three groups - <i>regional events</i>, which are available at the Regional, State, and National Conferences; <i>state events</i>, which are available at the State and National Conferences; and <i>Virginia events</i>, which are only available at the Virginia State Conference (Technosphere).</p>

<h4>Regional Events</h4>

<ul>
    <li><b>Career Preparation</b>:  Competitors perform research on top technology-related careers, as rated by the Bureau of Labor Statistics.  Students prepare resumes and cover letters, and must perpare for a sample on-site job interview related to the careers.</li>

    <li><b>Computer Aided Design (CAD) 2D - Architecture</b>:  Students, aided by design software, create two-dimensional representations of patterns, such as floor plans, elevation drawings, and architectural detailing.</li>

    <li><b>Computer Aided Design (CAD) 3D - Engineering</b>:  Students create three-dimensional computer models of machinery or engineering implements, such as tools, parts, or manufactured products.</li>

    <li><b>Debating Technological Issues</b>:  Teams of two prepare to be pitted against each other in debates, placed on opposite sides of a technological subtopic.</li>

    <li><b>Desktop Publishing</b>:  Competitors produce a 'publication' including a news release, a three-column newsletter, and a poster, and work on-site to solve a problem, demonstrating proficiency at using computers to manage in-house publishing materials.</li>

    <li><b>Digital Video Production</b>:  Teams use their multimedia expertise to craft a creative, appealing, and informative digital video or film that highlights the year's published theme.</li>

