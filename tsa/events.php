    <li><b>Technical Sketching &amp; Application</b>:  Competitors take a written test to qualify for semifinals, where they show their prowess at solving graphical engineering problems through the use of standard drafting techniques.</li>

    <li><b>Technology Bowl</b>:  Teams of three participate in a "bowl-style" event with questions centering around technology.  First, the team members individually take a written exam.  The team's scores are added, and the top twelve teams advance to a tournament-style bracket.</li>

    <li><b>Technology Problem Solving</b>:  Teams work on-site, using only the limited materials and tools provided to develop a solution to a given technological problem.</li>

    <li><b>VEX Robotics</b>:  VEX is a head-to-head robotics contest, in which teams design and build robots to complete tasks on the VEX Robotics Competition game field. More information can be found <a href="http://www.tsaweb.org/Vex-Robotics-Competition">here</a>.</li>

    <li><b>Webmaster</b>:  Teams of up to five work to produce a website showcasing their TSA chapter as well as TSA as a whole.  In addition, teams must also create a site to match a Design Brief that changes every year.  (The 2014 design brief is "How to Build Your First Web Site".)</li>
</ul>

<h4>Virginia Events</h4>

<ul>

    <li><b>Principles of Technology</b>:  Modeled off of the "Principles of Technology" course endorsed by TSA, the Principles of Technology event is a combination written and hands-on examination of students' scientific and technical knowledge.  Students work in teams of up to five to complete the examination, which covers a range of topics including chemistry, physics, and electronics.</li>            

    <li><b>Geospatial Technology</b>:  Competitors profile a geographical area of interest and solve a problem with it as a context, and, on-site, demonstrate prowess with geospatial technology software and data acquisition, as well as the ability to use such data to develop solutions to environmental and social issues.</li>
</ul>