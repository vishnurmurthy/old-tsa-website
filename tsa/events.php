    <li><b>Dragster Design</b>:  Competitors design and build a dragster, powered by CO2.</li>

    <li><b>Essays on Technology</b>:  Competitors conduct research in a technological field and use the information and insight gain through their research to write an essay on one of several subtopics, chosen on-site.</li>

    <li><b>Extemporaneous Presentation</b>:  Competitors draw a card, and, after fifteen minutes, give a three to five minute, spontaneous, organic, extemporaneous speech on the technology or topic designated by the card.</li>

    <li><b>Flight Endurance</b>:  Flight Endurance: Competitors build, fly, and tweak a rubber band powered model aircraft, with the goal of maximizing flight endurance in a contained airspace.</li>

    <li><b>Manufacturing Prototype</b>:  Teams design and then manufacture a prototype of a product, and go on to describe how manufacturing of the product would be implemented large-scale in a state-of-the-art facility.</li>

    <li><b>Music Production</b>:  Teams compose an inspiring, motivating, emotional, fabulous, exciting musical piece, designed to be played during the TSA national conference.</li>

    <li><b>Photographic Technology</b>:  Competitors take photos, process them, and then produce digital prints, depicting the year's published theme. On-site, semifinalists must demonstrate their skills by capturing images and using multimedia programs to develop a presentation.</li>

