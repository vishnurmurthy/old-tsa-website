    <li><b>Prepared Presentation</b>:  Competitors prepare and then deliver an oral presentation, with visual / audio enhancement, based on the year's published theme.</li>

    <li><b>Promotional Graphics</b>:  Competitors design and then create a graphic to promote participation in a selected TSA event.</li>

    <li><b>System Control Technology</b>:  Teams work on-site, developing a computer-controlled model to solve a problem, usually with an industrial theme. Team members analyze the problem, design and build the computerized mechanical model, and explain the program and mechanical features of their solution.</li>

    <li><b>Transportation Modeling</b>:  Competitors, using their engineering prowess, design and build a scale model of a vehicle, powered by CO2, that meets the year's published design theme.</li>

    <li><b>Video Game Design</b>:  Teams of up to five are tasked with building an E-rated video game.  The game may have a subject of the team's choosing, but extra points are awarded for exemplary educational or social value.</li>
</ul>

<h4>State Events</h4>

<ul>
    <li><b>Animatronics</b>:  Teams demonstrate their mechanical, electronic, and engineering prowess by designing, fabricating, and controlling an animatronics device which, with accompanying sound, light, and environment, communicates, entertains, or demonstrates a concept, subject, or theme. </li>

    <li><b>Architectural Renovation</b>:  Teams create architectural plans and related materials for an annual design challenge and build models, both physical and computerized, to accurately and efficiently display their design.</li>

    <li><b>Biotechnology Design</b>:  Teams find a contemporary biotechnology issue, related to the annual published theme, and perform research on it, culminating in the development of a solution, displayed using a multimedia presentation.</li>

    <li><b>Children's Story Telling</b>:  Teams create an story with educational value and present their work in an innovative way to capture audience attention.

    <li><b>Computer Numerical Control (CNC) Production</b>:  Teams design and then use a CNC machine to produce mousetrap car parts, aiming to maximize the distance the car can travel.</li>
        
