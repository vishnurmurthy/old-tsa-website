<h4>National Conference</h4>

<p>The National Conference is held yearly at the end of June in a different host city every year.  Members may participate in any events in which they qualified at their respective State Conferences, in addition to choosing new events in which they have not participated at either the Regional or State Conferences (however, some events such as Video Game Design require participants to have qualified at the State Conference).</p>