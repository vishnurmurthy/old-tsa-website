<p>Every June Thomas Jefferson HS has a one day celebration known as Jefferson Day or J-Day! There are very short classes, giving Student Government Association time to set up and then at 11 AM the fun begins. Students can show off their musical talents in band or solo performances, and various clubs sell food items, drinks, or other merchandise suchs as tshirts to fundraise. There are outdoor activities such as moon bounces or water balloon fights and indoor fun like video games and a board game room.</p>

<p>TJ TSA will be hosting a game room in Mr. Piccione's room (146). We plan on having four different set ups of either the Wii or Wii U playing Super Smash Bros. and Mario Kart. We will fundraise by charging students based on time spent in the game room, charging 50&cent; for soda, and having several video game tournaments with seperate entry fees. There will be prizes for winners of the tournaments. Our fundraising goal for this event is about $400. Last year we had a booth that sold fried oreos, bannanas, donuts, and twinkies and we allowed students to bring other food items to us that they wanted to deep fry.</p>

<h3>Conferences</h3>

<p>TSA offers conferences at the regional, state, and national levels.  TJ attends all three for our area - the Northern Regional Fair, the Virginia State Conference (Technosphere), and the National Conference.</p>

<h4>Northern Regional Fair</h4>

<p>The Northern Regional Fair is held annually at at the beginning of March; this year, the Northern Regional Fair was held at Mt. Vernon High School in Fairfax County.  The fair provides the first opportunity for students to participate in many of TSA's competitive events, though the semifinalist interviews (where applicable) are not performed.</p>

<h4>Virginia State Conference (Technosphere)</h4>

<p>Technosphere takes place every year at the end of April or at the beginning of May.  This year, Technosphere is being held at the Hampton Convention Center.  Chapter members may compete in any event in which they placed at regionals in addition to new events from an additional eighteen available.</p>

<p>Technosphere uniquely hosts two Virginia-only events (Principles of Technology and Geospatial Technology).</p>

