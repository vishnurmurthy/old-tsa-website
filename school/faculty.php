<h3>Technology Electives</h3>

<p><b>Mr. DC, Mr. Baeling,</b> and <b>Mr. Kemp</b> are the teachers of the robotics, prototyping, and energy systems electives and research labs respectivley. They sponsor various technology clubs including the First Robotics Team which competes at the state and sometimes national level. Mr. DC is very passionate about superheroes and always keeps a Green Lanturn action figure on his messy desk. Mr. Baeling is also in charge of the mens wrestling team. Between the three of them, they have 15 3D printers at their disposal that are always working to make new parts for the labs or on loan to students to use for projects. </p>

