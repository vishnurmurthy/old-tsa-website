<h3>Computer Science</h3>

<p>The professors in our Computer Science department are highly qualified individuals. <b>Dr. Torber, Mr. Stueben,</b> and <b>Dr. Gabor</b> are the three computer science teachers in charge of upper level course work (AI and Parallel Computing) as well as Computer Systems Research Lab. Dr. Gabor went to Stanford, MIT, and Princeton, and is an avid ballroom dancer (he sponsors the TJ Ballroom dance team). Both him and Mr. Stueben love to play chess and there is often a board set up in the Computer Systems Lab showing their latest game. In addtion to chess, Mr. Stueben is a movie buff who frequently shows clips relating to computers and robots (terminator, iRobot, etc.) in his AI classes. Dr. Torbert is an active traveler who has explored the technology scene in China quite thoroughly.</p>

<p>Finally, <b>Mr. Kosek,</b> who used to teach mainly freshmen Design and Technology, has started to teach web and mobile application development courses. This year was the first year in which these courses were offered. Mr. Kosek is one of the most approachable teachers in the school. He likes to credit some of that to his modern fashion choices, as he likes to often wear exotic ties and bright flannel shirts. His specialty is teaching Android App Development which is the centerpiece of his new Mobile App course.</p>

